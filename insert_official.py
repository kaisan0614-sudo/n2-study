def insert_official():
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()

    existing = c.execute("SELECT COUNT(*) FROM questions WHERE question LIKE '【問題%'").fetchone()[0]
    if existing > 0:
        print(f"官方樣題已存在 {existing} 題，跳過插入。若要重新插入，請先手動刪除。")
        conn.close()
        return

    c.executemany(
        "INSERT INTO questions (type,context,question,option_a,option_b,option_c,option_d,correct_answer,explanation) VALUES (?,?,?,?,?,?,?,?,?)",
        [(q["type"], q.get("context"), q["question"], q["option_a"], q["option_b"],
          q["option_c"], q["option_d"], q["correct_answer"], q.get("explanation"))
         for q in OFFICIAL_QUESTIONS]
    )
    conn.commit()
    conn.close()
    print(f"成功插入 {len(OFFICIAL_QUESTIONS)} 道官方樣題。")


if __name__ == "__main__":
    insert_official()
