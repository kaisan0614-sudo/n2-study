    c = conn.cursor()

    existing = c.execute("SELECT COUNT(*) FROM questions").fetchone()[0]
    c.executemany(
        "INSERT INTO questions (type,context,question,option_a,option_b,option_c,option_d,correct_answer,explanation) VALUES (?,?,?,?,?,?,?,?,?)",
        [(q["type"], q.get("context"), q["question"], q["option_a"], q["option_b"],
          q["option_c"], q["option_d"], q["correct_answer"], q.get("explanation"))
         for q in BATCH2]
    )
